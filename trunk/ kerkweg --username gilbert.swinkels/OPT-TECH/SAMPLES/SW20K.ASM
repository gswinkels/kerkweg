TITLE   SW20K
;
;OPT-TECH SORT STATIC WORK AREA DEFINITION
;
;DECLARE THE FIRST WORD OF THE AREA PUBLIC
;IT CONTAINS THE NUMBER OF AVAILABLE PARAGRAPHS (16 BYTE AREAS)
;
	PUBLIC	SW_PARA
	PUBLIC	@PGM_END
;
;SEGMENT MUST BE PARAGRAPH ALIGNED. IT CAN HAVE ANY NAME.
;
SW_CODE		SEGMENT	PARA 'CODE'
;
;FIRST WORD OF AREA IS NUMBER OF PARAGRAPHS OF AREA AVAILABLE BELOW
;
SW_PARA		DW	SIZE SW_AREA / 16
;
;DEFINE THE AREA FOR OPT-TECH SORT TO WORK IN.
;IF DEFINING GREATER THAN 64K, THEN MULTIPLE SEGMENTS ARE NECESSARY
;THESE AREAS MUST BE CONTIGUOUS IN MEMORY
;
SW_AREA		DB	20480 DUP(?)	;ACTUAL WORK AREA
;
@PGM_END	DB	'*'
SW_CODE		ENDS
	END
